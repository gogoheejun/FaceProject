package com.example.faceproject;

public class UserItem {
    String no;
    String userID;
    String password;
    String profileUrl;

    public UserItem() {
    }

    public UserItem(String no, String userID, String password, String profileUrl) {
        this.no = no;
        this.userID = userID;
        this.password = password;
        this.profileUrl = profileUrl;
    }
}
